package com.nutrispec.nutrispecapp.models;

public class Blog {
	
	private String id;
	private String nutritionist_id;
	private String blog_title;
	private String blog_article;
	private String blog_image;
	private String blog_date;
	private String blog_time;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNutritionist_id() {
		return nutritionist_id;
	}
	public void setNutritionist_id(String nutritionist_id) {
		this.nutritionist_id = nutritionist_id;
	}
	public String getBlog_title() {
		return blog_title;
	}
	public void setBlog_title(String blog_title) {
		this.blog_title = blog_title;
	}
	public String getBlog_article() {
		return blog_article;
	}
	public void setBlog_article(String blog_article) {
		this.blog_article = blog_article;
	}
	public String getBlog_image() {
		return blog_image;
	}
	public void setBlog_image(String blog_image) {
		this.blog_image = blog_image;
	}
	public String getBlog_date() {
		return blog_date;
	}
	public void setBlog_date(String blog_date) {
		this.blog_date = blog_date;
	}
	public String getBlog_time() {
		return blog_time;
	}
	public void setBlog_time(String blog_time) {
		this.blog_time = blog_time;
	}
	
	
	
}
